"""
18- Faça uma função que receba por parâmetro dois valores: X e Z.
Calcule e retorne o resultado de X elevado a Z.
Obs: Não utilizar nenhuma função pronta de exponenciação.
"""


def potencia(base, expoente=2):
    return base ** expoente


print(potencia(2))
print(potencia(3, 3))
print(potencia(10, 10))








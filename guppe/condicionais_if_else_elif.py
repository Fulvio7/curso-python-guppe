"""
Estruturas condicionais
if, else e elif
se, senão e senãose
"""

idade = 27

if idade < 18:
    print('Menor de idade')
elif idade == 18:
    print('Tem 18 anos')
elif idade == 42:
    print('Tem 42 anos')
else:
    print('Maior de idade')



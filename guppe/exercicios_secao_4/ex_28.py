"""
28- Faça a leitura de três valores e apresente como resultado a
soma dos quadrados dos três valores lidos.
"""

n1 = float(input('Digite o valor de n1 '))
n2 = float(input('Digite o valor de n2 '))
n3 = float(input('Digite o valor de n3 '))

total = (n1 ** 2) + (n2 ** 2) + (n3 ** 2)

print(f'A soma dos quadrados de n1, n2 e n3 é: {total} ')

"""
3- Peça ao usuário para digitar três valores inteiros e imprima
a soma deles.
"""
n1 = int(input('Digite o primeiro número inteiro '))
n2 = int(input('Digite o segundo número inteiro '))
n3 = int(input('Digite o terceiro número inteiro '))

total = n1 + n2 + n3

print(f'Realizando a soma deles temos \n{n1} + {n2} + {n3} = {total} ')

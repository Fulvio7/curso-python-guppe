"""
39- Um prêmio de R$ 780.000,00 será dividido entre três ganhadores,
sendo que:
- o primeiro recebe 46%;
- o segundo recebe 32%;
- o terceiro recebe o restante;
Calcule e imprima o valor que cada ganhador receberá.
"""

ganhador_1 = 780_000 * 0.46
ganhador_2 = 780_000 * 0.32
ganhador_3 = 780_000 * 0.22

print('A divisão do prêmio de R$ 780.000,00 ficou assim ')
print(f'O primeiro recebe R$ {ganhador_1:.2f} ')
print(f'O segundo recebe R$ {ganhador_2:.2f} ')
print(f'O terceiro recebe R$ {ganhador_3:.2f} ')


"""
None

Em Python, None é um tipo de dado sem tipo.
É especificado sempre com a inicial maiúscula.

Certo: None
Errado: none

Utilizamos quando queremos criar uma variável e inicializá-la,
mas ainda não sabemos o seu tipo final.

Sempre é considerado como False.

"""


numeros = None
print(numeros)
print(type(numeros))

